/*                                                                                                                                           */
//La déclaration d'une Pile  d'entiers :
//_____________________________________________________________________________________________________________________________________________
 typedef struct element1
  {
  	int v;
  	struct element1 *nxt;
  }element1,*pile;
//_____________________________________________________________________________________________________________________________________________
//Fonction 1: Empiler (ajouter en tete) :
//_____________________________________________________________________________________________________________________________________________
 pile empiler(pile p,int n)
  {
  	pile np=(pile)malloc(sizeof(element1));
  	 np->v=n;
  	 np->nxt=p;
  	return np;
  }
//_____________________________________________________________________________________________________________________________________________
//Fonction 2: Depiler (supprimer en tete) :
 pile depiler(pile p)
  {
  	pile tmp=p;
  	 if(p!=NULL)
  	  {
  	  	p=tmp->nxt;
  	  	free(tmp);
	  }
	return p;
  }
//_____________________________________________________________________________________________________________________________________________
//Fonction 3 Affichage :
//_____________________________________________________________________________________________________________________________________________
 void afficher_pile(pile p)
  {
  	pile tmp=p;
  	 while(tmp!=NULL)
  	  {
  	  	printf("%d\t",tmp->v);
  	  	tmp=tmp->nxt;
	  }
  }
//_____________________________________________________________________________________________________________________________________________
//Fonction 4: Valeur du sommet :
//_____________________________________________________________________________________________________________________________________________
 int sommet_pile(pile p)
  {
  	return p->v;
  }
//_____________________________________________________________________________________________________________________________________________
//Fonction 5: Vider la pile :
//_____________________________________________________________________________________________________________________________________________
 pile vider_pile(pile p)
  {
  	while(p!=NULL)
  	 p=depiler(p);
  	return p;
  }
